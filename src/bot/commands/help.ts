/** /help command handler — comprehensive feature guide */
import { env } from '../../config/env';
import type { Ctx } from '../types';

function buildHelpText(): string {
  const bot = env.BOT_USERNAME;
  return `\
<b>📚 Что умеет бот</b>

<b>💰 Запись расходов</b>
Просто отправь сообщение с суммой:
<code>100$ еда обед</code>
<code>190 евро Алекс кулёма</code>
<code>1900 RSD транспорт такси</code>

Математика в суммах:
<code>10*3$ еда</code> → запишет $30
<code>100+50€ транспорт</code> → запишет €150

<b>📸 Фото чеков</b>
Отправь фото чека — бот разберёт позиции и предложит подтвердить каждую. Нужно чтобы был чётко виден QR-код или текст с суммами.

<b>🤖 AI-ассистент</b>
Напиши <code>@${bot} вопрос</code> — бот ответит на основе твоих расходов. Умеет не только отвечать, но и действовать:
• Записать/удалить расход
• Управлять бюджетом и категориями
• Синхронизировать с Google Sheets

<b>🧮 AI-калькулятор</b>
ChatGPT, Claude и другие нейронки регулярно считают неправильно. У нашего бота настоящий калькулятор — спроси через <code>@${bot}</code>:
<i>«сколько мы потратили на еду в евро?»</i>
<i>«100$ + 70€ + 2000 RSD — сколько в евро?»</i>
<i>«раздели ужин на троих»</i>
<i>«500€ минус 10% скидка»</i>

<b>📊 Финансовые советы</b>
/advice — детальный разбор трат. Бот находит:
• Приближение к лимиту бюджета
• Аномальный рост в категории
• Резкое ускорение расходов
• Еженедельные и месячные тренды

<b>📋 Работа с таблицей</b>
/spreadsheet — ссылка на Google-таблицу
/sync — загрузить расходы из таблицы в бота (если правил руками)
/push — выгрузить расходы из бота в таблицу
/budget sync — синхронизировать бюджеты из таблицы

<b>✏️ Ручные правки в таблице</b>
Можешь менять суммы, категории и комментарии прямо в таблице. Но учти:
• Столбец <b>EUR (calc)</b> — статическое число, НЕ формула. Если меняешь сумму — пересчитай EUR вручную.
• После правок в таблице запусти /sync чтобы бот подхватил изменения.
• Если менял бюджеты — запусти /budget sync.

Все команды — набери <code>/</code> в поле ввода и выбери из списка.

Подробности и другие возможности всегда можно уточнить через <code>@${bot}</code> — просто спроси.`;
}

export async function handleHelpCommand(ctx: Ctx['Command']): Promise<void> {
  await ctx.send(buildHelpText(), { parse_mode: 'HTML' });
}
